#Square and Cube of a Number - Ask the user for a number and display its square and cube

#Square = multiply number by itself
#cube = multiply number by itself twice

number=int(input("Please Enter a Number : "))

square=number*number    # number**2
cube=number*number*number   # number**3
print("Square of Given number is : ",square)
print("Cube of Given number is : ",cube)